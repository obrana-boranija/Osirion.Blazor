namespace Osirion.Blazor.Components;

/// <summary>
/// Defines content alignment options
/// </summary>
public enum ContentAlignment
{
    /// <summary>
    /// Top-left alignment
    /// </summary>
    TopLeft,

    /// <summary>
    /// Top-center alignment
    /// </summary>
    TopCenter,

    /// <summary>
    /// Top-right alignment
    /// </summary>
    TopRight,

    /// <summary>
    /// Center-left alignment
    /// </summary>
    CenterLeft,

    /// <summary>
    /// Center alignment (both horizontally and vertically)
    /// </summary>
    Center,

    /// <summary>
    /// Center-right alignment
    /// </summary>
    CenterRight,

    /// <summary>
    /// Bottom-left alignment
    /// </summary>
    BottomLeft,

    /// <summary>
    /// Bottom-center alignment
    /// </summary>
    BottomCenter,

    /// <summary>
    /// Bottom-right alignment
    /// </summary>
    BottomRight
}