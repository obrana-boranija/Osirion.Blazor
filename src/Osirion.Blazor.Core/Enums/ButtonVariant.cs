namespace Osirion.Blazor.Components;

/// <summary>
/// Defines button style variants
/// </summary>
public enum ButtonVariant
{
    /// <summary>
    /// Primary button style (default)
    /// </summary>
    Primary,

    /// <summary>
    /// Secondary button style
    /// </summary>
    Secondary,

    /// <summary>
    /// Success button style (typically green)
    /// </summary>
    Success,

    /// <summary>
    /// Danger button style (typically red)
    /// </summary>
    Danger,

    /// <summary>
    /// Warning button style (typically yellow/orange)
    /// </summary>
    Warning,

    /// <summary>
    /// Info button style (typically blue)
    /// </summary>
    Info,

    /// <summary>
    /// Link/text button style (no background)
    /// </summary>
    Text,

    /// <summary>
    /// Outlined primary button
    /// </summary>
    OutlinedPrimary,

    /// <summary>
    /// Outlined secondary button
    /// </summary>
    OutlinedSecondary
}